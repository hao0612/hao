public class Test算术运算符 {
    public static void main(String[] args) {
        int ia =10;
        int ib = 2;
        int ic = ia + ib ;
        System.out.println(ia+ib);
        System.out.println("ic="+ic);
        System.out.println(ia-ib);
        System.out.println(ia*ib);
        System.out.println(ia/ib);
        System.out.println(ia%ib);
        System.out.println("----------------");
        System.out.println(5/2);
        System.out.println((double)5/2);

        System.out.println("-----------");

    }
}
